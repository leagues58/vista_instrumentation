				 This can result in a more informed application of the data to the task of planning track maintenance.
				 Access to the design also means that a railway is no longer limited to the system supplier for support.
				 Given full engineering documentation, many companies would be able to quote and provide support for both the hardware and software.
				 Of course, Vista Instrumentation will be glad to quote on-going support if requested.</p>

